/**
 * Programa que declare varibles tipo char y tipo string.
 * Mostrar por pantalla todas juntas en la misma línea y con una sola sentencia.
 * ¿Es posible?
 * 
 * @author: Raquel López
 */
public class T02Ejercicio07 {
    public static void main(String[] args) {
        char letraa = 'A';
        char letray = 'y';
        String cerveza = " tomar cerveza fresquita";
        String dormir = " después a dormir.";

        System.out.println(letraa + cerveza + " " + letray + dormir);

        // Para que la frase tenga coherencia, hay que añadir un espacio entre el string cerveza y el char letray.

    }
}
