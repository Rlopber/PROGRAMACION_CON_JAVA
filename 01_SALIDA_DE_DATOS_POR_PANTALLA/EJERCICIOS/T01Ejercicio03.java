/**
* Programa que muestre por pantalla 10 palabras en inglés junto a su traducción.
* Las palabras deben estar distribuidas en dos columnas y alineadas a la izquierda.
*
* @author: Raquel Lopez
*/

public class T01Ejercicio03 {
   public static void main(String[] args) {
       System.out.printf("%-1s %11s\n", "Inglés", "Español");
       System.out.printf("%-1s %11s\n", "Cat", "Gato");
       System.out.printf("%-1s %12s\n", "Car", "Coche");
       System.out.printf("%-1s %12s\n", "Rain", "Lluvia");
   }
}
