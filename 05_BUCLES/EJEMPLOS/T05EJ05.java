public class T05EJ05 {
  public static void main(String[] args) {
    System.out.println("Este programa te va a mostrar los números del 1 al 10.");
    int i = 1;
    
    do {
        System.out.print(i + " ");
        i++;
    } while (i < 11);
  }  
}
